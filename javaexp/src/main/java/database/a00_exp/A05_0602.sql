	7. 사원 번호의 3으로 나누어 %로 해서, 0 ==> 0%, 1 ==>10%, 2==>20%
   		보너스를 정하여, sal기준으로 원금+보너스 합산을 출력하되,
   		10자리단위로 반올림 처리하여 출력하세요
   		사원명, 급여, 보너스(%), 보너스, 총계
	
		*/
			SELECT ename, sal, MOD(empno,3)*10 "보너스 (%)",
			sal*MOD(empno,3)/10 "보너스",
			sal + (sal*MOD(empno,3)/10) "총계"
			FROM emp;
		/*
		
		# 보너스 기준
		0 ==> 0%, 1 ==> 10%, 2 ==> 20%
		MOD(empno,3)*10 : 나머지 값을 기준으로 보너스 % 표시
		sal*MOD(empno,3)/10 : 실제 연산할 %에 해당하는 실수값을 sal와...
		sal + (sal*MOD(empno,3)/10) : 원금 + 보너스 총계

	8. 관리자 번호가 짝수인 사번과 관리자 번호와 급여를 출력하되, 급여는 100자리 수로 절삭하여 처리하세요.
	
