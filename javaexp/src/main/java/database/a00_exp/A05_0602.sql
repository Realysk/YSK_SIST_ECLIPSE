		숫자형 함수의 종류
			1. 자리수 처리
				round(데이터,자리수) : 반올림
				trunc(데이터,자리수) : 절삭
					자리수 + : 소숫점이하 자리수
					자리수 - : 왼쪽으로 10, 100, 1000...
			2. 정수로 처리
				ceil(데이터) : 올림
