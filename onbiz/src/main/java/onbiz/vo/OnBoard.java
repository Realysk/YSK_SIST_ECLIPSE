package onbiz.vo;

import java.util.Date;

import org.springframework.web.multipart.MultipartFile;


public class OnBoard {
	
	private int cnt; // 한 페이지에 표시할 데이터 건수
	private int bd_no; // 게시물 번호 (pk)
	private int bd_refno; // 게시물 상위번호
	private String bd_name; // 게시물 제목
	private String bd_content; // 게시물 내용
	private int bd_viewcnt; // 게시물 조회수
	private Date bd_regdte; // 게시물 등록일
	private Date bd_uptdte; // 게시물 수정일
	private String mem_no; // 게시물 작성자 (fk)
	private String bd_fname; // 파일명
	private MultipartFile report; // 첨부 파일을 받을 수 있는 객체
	
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getBd_no() {
		return bd_no;
	}
	public void setBd_no(int bd_no) {
		this.bd_no = bd_no;
	}
	public int getBd_refno() {
		return bd_refno;
	}
	public void setBd_refno(int bd_refno) {
		this.bd_refno = bd_refno;
	}
	public String getBd_name() {
		return bd_name;
	}
	public void setBd_name(String bd_name) {
		this.bd_name = bd_name;
	}
	public String getBd_content() {
		return bd_content;
	}
	public void setBd_content(String bd_content) {
		this.bd_content = bd_content;
	}
	public int getBd_viewcnt() {
		return bd_viewcnt;
	}
	public void setBd_viewcnt(int bd_viewcnt) {
		this.bd_viewcnt = bd_viewcnt;
	}
	public Date getBd_regdte() {
		return bd_regdte;
	}
	public void setBd_regdte(Date bd_regdte) {
		this.bd_regdte = bd_regdte;
	}
	public Date getBd_uptdte() {
		return bd_uptdte;
	}
	public void setBd_uptdte(Date bd_uptdte) {
		this.bd_uptdte = bd_uptdte;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	public String getBd_fname() {
		return bd_fname;
	}
	public void setBd_fname(String bd_fname) {
		this.bd_fname = bd_fname;
	}
	public MultipartFile getReport() {
		return report;
	}
	public void setReport(MultipartFile report) {
		this.report = report;
	}
	
}
