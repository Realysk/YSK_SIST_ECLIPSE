package Project_SIST.Java.A02_Marpple;
 
public class Custom_squareexp {
 
    public static void main(String[] args) {
        // 별(*)로 사각형 만들기
 
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}